import java.util.Arrays;

public class Main {
    public static void main(String[] args) {
        Abiturient[] абітуріенти = Abiturient.створитиМасивАбітуріентів();

        // a) список абітурієнтів, що мають незадовільні оцінки
        System.out.println("Список абітурієнтів з незадовільними оцінками:");
        for (Abiturient абітуріент : абітуріенти) {
            if (абітуріент.маєНезадовільніОцінки()) {
                System.out.println(абітуріент);
            }
        }

        // b) список абітурієнтів, у яких сума балів вище заданої
        int заданаСумаБалів = 20;
        System.out.println("Список абітурієнтів з сумою балів вище " + заданаСумаБалів + ":");
        for (Abiturient абітуріент : абітуріенти) {
            if (абітуріент.сумаБалів() > заданаСумаБалів) {
                System.out.println(абітуріент);
            }
        }

        // c) вибрати задане число n абітурієнтів, що мають саму високу суму балів
        int n = 3;
        Arrays.sort(абітуріенти, (a1, a2) -> Integer.compare(a2.сумаБалів(), a1.сумаБалів())); // Сортування за спаданням суми балів
        System.out.println("Перші " + n + " абітурієнтів з найвищою сумою балів:");
        for (int i = 0; i < Math.min(n, абітуріенти.length); i++) {
            System.out.println(абітуріенти[i]);
        }
    }
}
